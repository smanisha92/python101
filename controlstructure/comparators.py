""" == -> Equality operator
!= -> Not equal to
> -> Greater than
< -> Less Than
<= Less than or equal to
>= Greater than or Equal to"""
print("*********** COMPARE OPERATORS EXAMPLES *************")
bool_one = 10 == 10
print(bool_one)
bool_one = 10 != 10
print(bool_one)
bool_one = 10 > 10
print(bool_one)
bool_one = 10 >= 10
print(bool_one)
bool_one = 10 < 10
print(bool_one)
bool_one = 10 <= 10
print(bool_one)

